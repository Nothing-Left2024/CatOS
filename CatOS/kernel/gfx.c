/* ===== 文本缓冲区 (图形模式) ===== */
/* 用于记录屏幕上每个位置的字符和颜色, 擦除光标时可以精确重绘原字符 */
/* 256x80 支持 VBE 1920x1080 (8x16=240x67) 和 1280x1024 (8x16=160x64) */
#define MAX_TEXT_COLS 256
#define MAX_TEXT_ROWS 80
static char s_text_buf[MAX_TEXT_ROWS * MAX_TEXT_COLS];       /* 字符 */
static t_8  s_color_buf[MAX_TEXT_ROWS * MAX_TEXT_COLS];      /* 颜色属性 */

